<script>
import KeyValue from '@/components/form/KeyValue';

export default {
  components: { KeyValue },

  props: {
    spec: {
      type:     Object,
      required: true,
    },
    visible: {
      type:    Boolean,
      default: false
    },
    mode: {
      type:     String,
      default: 'view'
    }
  },

  data() {
    return { dialogVisible: false, annotations: {} };
  },

  methods: {
    open() {
      this.annotations = Object.assign({}, this.spec?.metadata?.annotations);
    },
    handleClose() {
      this.$emit('close');
    },
    save() {
      this.$set(this.spec.metadata, 'annotations', this.annotations);
      this.$emit('update');
      this.$emit('close');
    },
  }
};
</script>

<template>
  <el-dialog
    title="Edit Annotations"
    :visible="visible"
    width="50%"
    :before-close="handleClose"
    @open="open"
  >
    <KeyValue
      v-if="visible"
      key="annotations"
      v-model="annotations"
      :mode="mode"
      :initial-empty-row="true"
      :pad-left="false"
      :read-allowed="false"
    />
    <span slot="footer">
      <button class="btn role-secondary btn-sm mr-20" @click="handleClose">
        Close
      </button>
      <button class="btn role-tertiary bg-primary btn-sm mr-20" @click="save">
        Save
      </button>
    </span>
  </el-dialog>
</template>

<style lang="scss" scoped>
.tip {
  font-size: 13px;
  font-style: italic;
}
</style>
