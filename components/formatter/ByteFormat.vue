<script>
import { getFileSize } from '@/utils/units';

export default {
  props: {
    value: {
      type:    Number,
      default: () => []
    }
  },
  computed: {
    formattedValue() {
      return getFileSize(this.value);
    }
  }
};
</script>

<template>
  <span>{{ formattedValue }}</span>
</template>
